        $mot_de_passe_hache = password_hash($mot_de_passe, PASSWORD_DEFAULT);
        
        // Afficher le mot de passe haché
        echo "<div class='mt-3'>";
        echo "<h4>Mot de passe haché :</h4>";
        echo "<p>" . htmlspecialchars($mot_de_passe_hache) . "</p>";
